<?php

namespace Effectra\Log;

/**
 * Class LogLevel
 *
 * This class defines constants representing different log levels.
 */
class LogLevel
{
    /**
     * Emergency log level.
     */
    const EMERGENCY = 'emergency';

    /**
     * Alert log level.
     */
    const ALERT = 'alert';

    /**
     * Critical log level.
     */
    const CRITICAL = 'critical';

    /**
     * Error log level.
     */
    const ERROR = 'error';

    /**
     * Warning log level.
     */
    const WARNING = 'warning';

    /**
     * Notice log level.
     */
    const NOTICE = 'notice';

    /**
     * Info log level.
     */
    const INFO = 'info';

    /**
     * Debug log level.
     */
    const DEBUG = 'debug';
}
